    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        BigDecimal precioMod = new BigDecimal(txtPrecioPMP.getText());
        Object moneda = cbMonedaPMP.getSelectedItem();
        String monedaMod = moneda.toString();
        int id = Integer.parseInt(idMatPrim);
        if(!txtPrecioPMP.equals("")){
            try{
                con = conexion.establecerConexion();
                PreparedStatement ps = con.prepareStatement("UPDATE MP SET Precio=?, Moneda=? WHERE id_Mp=?");
                ps.setBigDecimal(1, precioMod);
                ps.setString(2, monedaMod);
                ps.setInt(3, id);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Precio modificado");

            }catch(SQLException e){
                JOptionPane.showMessageDialog(null, e.toString());
            }
        }
            else{
            JOptionPane.showMessageDialog(null, "Ingrese todos los datos antes de guardar");
        }
        
        //----------------------CREAR LA TABLA----------------------
        
        DefaultTableModel modeloTabla = (DefaultTableModel) dgvAfectadas.getModel();
        modeloTabla.setRowCount(0);
        
        String codsql = "select Cotizacion.Id_Cotizacion as #Cot,Etapa.Nombre as Etapa, Clientes.Nombre as Cliente, Cotizacion.Fecha, Cotizacion.Descripcion from Etapa inner join Cotizacion on Cotizacion.Id_Etapa=Etapa.Id_Etapa inner join Detalle_Cotizacion on Cotizacion.Id_Cotizacion = Detalle_Cotizacion.Id_Cotizacion inner join Clientes on Clientes.Id_Cliente=Cotizacion.Id_Cliente where Detalle_Cotizacion.Id_Mp = ?";
        
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;
        int columnas;
        
        int[]anchos = {10,20,100,20,300};
        for(int i = 0; i < dgvAfectadas.getColumnCount(); i++){
            dgvAfectadas.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
        }
        
        try{
            con = conexion.establecerConexion();
            ps = con.prepareStatement(codsql);
            ps.setInt(1, id);
            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columnas = rsmd.getColumnCount();
            
            while(rs.next()){
                Object[] fila = new Object[columnas];
                for(int i = 0; i < columnas; i++){
                    fila[i] = rs.getObject(i+1);
                }
                modeloTabla.addRow(fila);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
            
        }
    }//GEN-LAST:event_btnModificarActionPerformed

