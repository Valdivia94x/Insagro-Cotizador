    public void cargarDatos(){
        PreparedStatement ps;
        ResultSet rs;
        
        try{
            con = conexion.establecerConexion();
            //con = conexion.establecerConexion();
            ps = con.prepareStatement("select * from MP where Activo = 1 and Id_Mp = " + idMatPrim + " order by Nombre Asc");
            rs = ps.executeQuery();
            while (rs.next())
                {
                    txtNombrePMP.setText(rs.getString("Nombre"));
                    txtNopartePMP.setText(rs.getString("N_Parte"));
                    txtDensidadPMP.setText(rs.getString("Densidad"));
                    txtPrecioPMP.setText(rs.getString("Precio"));
                    if (rs.getString("Moneda").equals("DLL"))
                    {
                        cbMonedaPMP.setSelectedItem("DLL");
                    }
                    else
                    {
                        cbMonedaPMP.setSelectedItem("MN");
                    }
                }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }
    
